            <summary>Code of Conduct</summary>
            <div class="answer">
            <h4>Code of Conduct for Everyone</h4>
            <ul>
                <li>No alcohol beverages, tobacco in any form, listening or watching electronic devices, fireworks, or weapons of any kind </li>
<li>No physical contact allowed between male and female campers </li>
<li>No clothing or accessories with inappropriate advertising </li>
<li>No hats or sunglasses in the tabernacle </li>
            </ul>
            <h4>Code of Conduct for Ladies</h4>
            <ul>
                <li>Modest <em>knee-length</em> skirts, dresses, and skirt-appearing culottes </li>
<li>Modest tops and blouses. </li>
<li>Swimwear – modest one or two piece (no bikinis) </li>
            </ul>
            <h4>Code of Conduct for Gentlemen</h4>
            <ul>
                <li>No sleeveless shirts or tank tops. </li>
<li>Modest knee-length shorts are allowed to be worn during the morning and afternoon activities </li>
<li>No jewelry (unless for medical identification purposes) </li>
<li>Swimwear – modest shorts, cut-offs, or trunks (no metal straps or bands due to damage on waterslides) </li>
            </ul>
            <h4>Highlighted Rules</h4>
            <ul>
                <li>No physical contact with opposite sex </li>
<li>Proper dress code will be enforced with a smile </li>
<li>No boys around girl’s cabins and no girls around boy’s cabins </li>
<li>Obey dress code to and from swimming hole </li>
<li>Respect and obey all authority</li>
<li>Proper respect, involvement, and behavior in tabernacle and during competition</li>
<li>Parent or youth pastor will be responsible to find a way home for a young person who: a) Goes  to the swimming hole without a lifeguard on duty or when opposite sex is there. b) Any inappropriate behavior between boys and girls</li>
<li>Have a great week</li>
            </ul>
